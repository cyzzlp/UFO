    /****************************************************************************************************
    函数名    :  OpenUSB_Board
    功能      :  打开CSC-USB卡的函数
    参数      :  deviceIndex:   USB的设备索引号
                 handle:    窗体句柄,当handle=NULL时，窗体未定义，不支持PnP事件
    返回值    :  1: 成功
                -1: 打开板卡失败
                -2: USB接口不是2.0接口
                -3: 开辟32M内存区失败
                -4: 板卡内时间大于当前计算机时间
                -5: 授权到期
    -----------------------------------------------------------------------------------------------------
    备注     :  若有一块CSC-USB卡, deviceIndex为0;
                若有二块CSC-USB卡, 第一块CSC-USB卡的索引号 deviceIndex为0;
                                   第二块CSC-USB卡的索引号 deviceIndex为1;
    -----------------------------------------------------------------------------------------------------
    ****************************************************************************************************/
    CSC_FUNC_INT OpenUSB_Board(int deviceIndex, void* handle);


    /****************************************************************************************************
    函数名:    LoadFPGA_FirmwareProgram
    功能    :  加载硬件程序
    参数    :  无
    返回值:    true:  硬件程序加载成功
               false: 硬件程序加载失败
    -----------------------------------------------------------------------------------------------------
    备注     : 1、该函数必须在OpenUSB_Board后立即调用
               2、调用这个函数时要将FpgaFirmware.rbf放在应用程序所在的目录下
    -----------------------------------------------------------------------------------------------------
    ****************************************************************************************************/
    CSC_FUNC_BOOL LoadFPGA_FirmwareProgram();

    /****************************************************************************************************
    函数名   : SetLaserMode
    功能     : 设置激光器模式
    参数     : laserType
                   0: YAG激光器
                   1: CO2激光器
                   2: IPG光纤激光器
                   3: SPI光纤激光器
                   默认值: 1
               standby:预电离  1,开启预电离; 0,取消预电离 默认值: 0
               frequency: 预电离频率, 范围:10-100Khz ,默认20 Khz
               pulseWidth: 预电离脉宽, 范围:10%-90% ,默认10%
    返回值   : true:  激光器设置成功
               false: 激光器设置失败
    -----------------------------------------------------------------------------------------------------
    备注     : 1、仅在CO2和IPG激光器下可设置 standby
               2、在LoadMarkParameters之前调用
               3、frequency 和 pulseWidth 只有在IPG下可设置
    -----------------------------------------------------------------------------------------------------
    ****************************************************************************************************/
    CSC_FUNC_BOOL SetLaserMode(int laserType, int standby, float frequency, float pulseWidth);

    /****************************************************************************************************
    函数名   : SetSystemParameters
    功能     : 初始化系统参数
    参数     :
    参数1 : double rangeX X轴标刻范围  单位:mm 默认值: 110
    参数2 : double rangeY Y轴标刻范围  单位:mm 默认值: 110
    参数3 : double markStep 标刻步长  单位:mm 默认值: 0.02
    参数4 : double jumpStep 跳转步长  单位:mm 默认值: 0.2
    参数5 : bool exchangeXY XY轴坐标是否互换 默认值: false
    参数6 : bool invertX X轴坐标是否反向 默认值: false
    参数7 : bool invertY Y轴坐标是否反向 默认值: false
    参数8 : int startMarkMode 启动打标的模式 0:自动启动 1:事件控制启动 默认值: 0
    返回值   : 1 : 函数执行成功
              -1 : 函数执行失败
    -----------------------------------------------------------------------------------------------------
    备注     :  该函数应在LoadMarkParameters函数之前调用，否则按默认值处理
    -----------------------------------------------------------------------------------------------------
    作者     :
    ****************************************************************************************************/
    CSC_FUNC_INT SetSystemParameters(double rangeX, double rangeY, double markStep, double jumpStep,
        bool exchangeXY, bool invertX, bool invertY, int startMarkMode);

